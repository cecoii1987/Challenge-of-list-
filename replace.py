# We will first create a variable called string that will contain the sentence
string = 'The!quick!brown!fox!jumps!over!the!lazy!dog.'

# We will create a variable with replace function and then print the new variable
string1 = string.replace('!', ' ')
print(string1)

# Next we will reprint the new sentence with upper() function
print(string1.upper())

# Then is asking us to print the sentence in reverse
print(string[::-1])
